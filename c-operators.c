#include <stdio.h>
/* all arithmetic operators:
   + Addition
   - Subtraction
   * Multiplication
   / Division
   % Remainder
   ++ Increment (increments by 1)
   -- Decrement (decrements by 1)
*/

int main(){
    int x = 12;
    int result = x + 8; // 12 + 8 = 20

    printf("Result is %d\n", result);
    printf("Result is now %d\n", result + 8); //should display 28

    

    return 0;
}